# Example of creating and using lists in Python

# A list of integers
my_list = [1, 2, 3, 4, 5]

# A list can contain different data types
mixed_list = [1, "hello", 3.14, True]

# You can access elements using index (starts at 0)
print("First item:", my_list[0])

# You can modify elements
my_list[1] = 99
print("Modified list:", my_list)

# Lists can be nested
nested_list = [1, [2, 3], 4]
print("Nested list element:", nested_list[1][0])
