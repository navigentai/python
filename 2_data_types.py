# Fundamental Data Types in Python

# int – Integer (whole number)
# float – Decimal number
# bool – Boolean (True or False)
# str – String (text)
# list – Ordered collection (mutable)
# tuple – Ordered collection (immutable)
# set – Unordered, unique collection
# dict – Key-value pairs
# None – Represents no value (null equivalent)

# Each of these types has unique properties and use cases.

# Integers and Floats
age = 30  # int
price = 19.99  # float

# Booleans
is_member = True
is_logged_in = False

# String
name = "Andrea"

# List (mutable)
fruits = ["apple", "banana", "mango"]

# Tuple (immutable)
coordinates = (10.5, 20.3)

# Set (unordered, unique)
colors = {"red", "blue", "green"}

# Dictionary (key-value pairs)
person = {"name": "Andrea", "age": 30, "is_member": True}

# None (no value)
result = None

print("--- DATA TYPES DEMO ---")
print("Age (int):", age)
print("Price (float):", price)
print("Is Member (bool):", is_member)
print("Name (str):", name)
print("Fruits (list):", fruits)
print("Coordinates (tuple):", coordinates)
print("Colors (set):", colors)
print("Person (dict):", person)
print("Result (None):", result)

# Print out the type of each variable
print("Type of age:", type(age))
print("Type of fruits:", type(fruits))
print("Type of colors:", type(colors))
print("Type of person:", type(person))
print("Type of result:", type(result))

